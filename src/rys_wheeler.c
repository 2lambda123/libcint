static void jacobi_gs(int n, double x, double *gs)
{
        int i;
        double g0 = 0.;
        double g1 = 1.;
        double g2;
        gs[0] = g1;
        for (i = 1; i < n; i++) {
                g2 = x - JACOBI_ALPHA[i - 1] * g1 - JACOBI_BETA[i - 1] * g0;
                gs[i] = g2;
                g0 = g1;
                g1 = g2;
        }
}

